
public class ToFindSumOfNaturalNumbers {
	public static void main(String args[]) {
		System.out.println("Program to find sum of natural number between 5 to 25");
		int i, sum = 0;
		for (i = 5; i <= 25; i++)
			sum = sum + i;
		System.out.println("Sum of natural number between 5 to 25 is : " + sum);

	}
}
